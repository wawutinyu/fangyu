"""
fangyu 后端配置模块
===================
功能：从 .env 文件和系统环境变量读取所有运行时配置。
设计原则：
- 所有配置集中管理，不分散在各个模块中。
- .env 文件仅用于开发环境，生产环境通过系统环境变量注入。
- 桌面版打包时可通过修改环境变量切换数据库路径/端口。

注意事项：
- 修改配置后必须重启服务（热重载会自动检测 .env 变化）。
- CORS_ORIGINS 必须包含前端实际运行的地址，否则跨域请求会被拦截。
- 转桌面版时，将 HOST 改为 127.0.0.1 避免端口暴露。
"""

import os
from pathlib import Path
from dotenv import load_dotenv


# ---------------------------------------------------------------------------
# 路径计算
# ---------------------------------------------------------------------------
# 项目根目录：backend/ 的父目录（即 ai-flow-canvas/）
# 用于定位 .env 文件、data 目录等相对路径资源。
# 注意：打包成 exe 后，BASE_DIR 需改为 sys.executable 所在目录。
BASE_DIR: Path = Path(__file__).resolve().parent.parent.parent

# 加载 .env 文件（仅开发环境）
# 生成环境建议通过系统环境变量或 Docker 环境变量注入配置。
# 桌面版打包后，.env 应放在可执行文件同级目录。
load_dotenv(BASE_DIR / ".env")


class Settings:
    """
    全局配置类
    ===========
    使用类变量的方式读取环境变量，所有取值都有默认值，
    确保即使没有 .env 文件也能正常运行（本地开发友好）。

    扩展方式：
    在 .env 中添加新变量 -> 在此类中添加同名属性 -> 在业务代码中通过
    `from app.core.config import settings` 引用。

    禁止：
    - 不要在业务代码中直接读取 os.getenv()，所有配置必须集中在此处。
    - 不要在运行时修改 settings 的属性（它是只读的配置快照）。
    """

    # -----------------------------------------------------------------------
    # 服务基础配置
    # -----------------------------------------------------------------------
    HOST: str = os.getenv("HOST", "0.0.0.0")
    """
    FastAPI 监听地址。
    - 开发环境：0.0.0.0（允许局域网访问，方便手机测试）。
    - 桌面版：127.0.0.1（仅本机访问，安全）。
    - 生产服务器：0.0.0.0（配合 Nginx 反向代理）。
    """

    PORT: int = int(os.getenv("PORT", "8000"))
    """
    FastAPI 监听端口。
    - 前端 Vite 代理默认转发到 8000，修改此处需同步修改 frontend/vite.config.js。
    - 桌面版建议使用 8000-9000 区间，避免占用常用端口（如 80/443）。
    """

    RELOAD: bool = os.getenv("RELOAD", "true").lower() == "true"
    """
    开发热重载开关。
    - 开发环境：true（代码修改后自动重启，无需手动停止再启动）。
    - 生产/桌面版：false（避免文件监听开销）。
    - 注意：uvicorn --reload 不支持 Windows 下某些文件系统事件。
    """

    # -----------------------------------------------------------------------
    # 数据库配置
    # -----------------------------------------------------------------------
    DATABASE_URL: str = os.getenv(
        "DATABASE_URL",
        f"sqlite+aiosqlite:///{BASE_DIR / 'data' / 'fangyu.db'}",
    )
    """
    数据库连接字符串。
    当前使用 SQLite（aiosqlite 异步驱动），适合单机/桌面场景。
    后续迁移到 PostgreSQL 只需修改此处：
      postgresql+asyncpg://user:pass@host:port/dbname

    注意事项：
    - SQLite 不支持并发写入，生产环境多用户需迁移到 PG。
    - SQLite 文件路径相对 BASE_DIR，确保 data/ 目录存在且有写入权限。
    - 桌面版打包后，数据库文件应放在用户数据目录（如 %APPDATA%）。
    """

    # -----------------------------------------------------------------------
    # 跨域配置
    # -----------------------------------------------------------------------
    CORS_ORIGINS: list[str] = os.getenv(
        "CORS_ORIGINS",
        "http://localhost:5173,http://localhost:5174,http://localhost:5175,file://",
    ).split(",")
    """
    允许跨域请求的来源列表。
    Vite 开发服务器默认 5173，如果端口冲突会自动递增（5174/5175）。
    生产环境应替换为实际的部署域名。

    注意事项：
    - 不允许使用 "*" 通配符（安全原因）。
    - 桌面版设置为 "http://localhost:*" 或直接给前端打包后的文件路径。
    - 新增前端地址时必须在列表中添加，否则 API 请求会被浏览器拦截。
    常见错误：
      前端报 "CORS Missing Allow Origin" → 检查此处是否包含前端地址。
    """

    # -----------------------------------------------------------------------
    # LLM API Key 配置
    # -----------------------------------------------------------------------
    OPENAI_API_KEY: str = os.getenv("OPENAI_API_KEY", "")
    DEEPSEEK_API_KEY: str = os.getenv("DEEPSEEK_API_KEY", "")
    ANTHROPIC_API_KEY: str = os.getenv("ANTHROPIC_API_KEY", "")
    """
    各 LLM 提供商的默认 API Key。
    用户可以在前端设置面板中覆盖这些值，覆盖后的值存储在 settings 表中。
    优先级：用户设置（数据库） > 环境变量 > 空字符串。

    注意事项：
    - 永远不要将 API Key 提交到 Git 仓库（.env 已在 .gitignore 中排除）。
    - 环境变量中的 Key 作为默认值/降级方案。
    - 桌面版打包后，Key 应通过应用设置界面配置，而非硬编码。
    """


# ---------------------------------------------------------------------------
# 单例导出
# ---------------------------------------------------------------------------
# 整个应用中只有一个 settings 实例，所有模块共享。
# 引用方式：from app.core.config import settings
# 不要在模块级别做 settings 的深拷贝，始终引用此单例。
settings = Settings()
